export class Employee {
    id: number;
    name: string;
    designation: string;
    department: string;
    Gender: string;
    sales: string;
   
  

    constructor(id, name, designation, department, Gender, sales) {
        this.id = id;
        this.name = name;
        this.designation = designation;
        this.department = department;
        this.Gender = Gender;
        this.sales = sales;
        

    }
}



